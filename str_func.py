def string_upper(string):
    """
    Функция которая получает на вход строку а возвращает строку со всеми заглавными буквами
    """
    return string.upper()


def string_title(string):
    """
    Функция которая получает на вход строку а возвращает строку где все слова с большой буквы
    """
    return string.title()
